#!/bin/zsh


python scripts/train.py \
  --remote-server-uri "http://mlflow-tracking.neural-tpps.svc.cluster.local" \
  --experiment-name synthea-split-1 \
  --run-name gru-rmtpp \
  --batch-size 64 \
  --data-dir /home/air/project/neural-tpps/data \
